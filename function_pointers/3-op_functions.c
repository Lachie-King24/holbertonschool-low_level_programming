#include "3-calc.h"

/**
 * op_add - adds two int
 * @a: first integer
 * @b: second integer
 * Return: result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two int
 * @a: first int
 * @b: second int
 * Return: result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two int
 * @a: first int
 * @b: second int
 * Return: Result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two int
 * @a: first int
 * @b: second int
 * Return: result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo two int
 * @a: first int
 * @b: second int
 * Return: result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
